import { NextRequest, NextResponse } from 'next/server';

// Utility function to verify Cloudflare Turnstile token
async function verifyTurnstile(token: string, ip?: string): Promise<boolean> {
  const secretKey = process.env.TURNSTILE_SECRET_KEY;

  if (!secretKey) {
    console.error('TURNSTILE_SECRET_KEY is not configured.');
    // In a production scenario, you might want to return false here
    // to prevent forms from being submitted without proper CAPTCHA.
    // For development, we might allow it to proceed.
    return true; // Allowing submission if key is missing (fail-open)
  }

  try {
    const formData = new URLSearchParams();
    formData.append('secret', secretKey);
    formData.append('response', token);
    if (ip) {
      formData.append('remoteip', ip);
    }

    const response = await fetch('https://challenges.cloudflare.com/turnstile/v0/siteverify', {
      method: 'POST',
      body: formData,
      // headers:
      //   {
      //     'Content-Type': 'application/x-www-form-urlencoded',
      //   },
    });

    const data = await response.json();

    if (!data.success) {
      console.warn('Turnstile verification failed:', data);
    }

    return data.success;
  } catch (error) {
    console.error('Turnstile verification error:', error);
    return false; // Fail closed in case of network error to Cloudflare
  }
}

export async function POST(request: NextRequest) {
  // Set CORS headers for Vercel deployment if needed, or rely on Next.js default behavior
  const headers = {
    'Content-Type': 'application/json',
    // 'Access-Control-Allow-Origin': '*', // Consider restricting to specific origins
    // 'Access-Control-Allow-Methods': 'POST, OPTIONS',
    // 'Access-Control-Allow-Headers': 'Content-Type',
  };

  try {
    const data = await request.json();
    const { name, email, message, turnstileToken, website } = data;

    // Honeypot check: If the hidden field 'website' is filled, it's a bot.
    if (website && website.length > 0) {
      console.warn(`Honeypot triggered. Bot detected. IP: ${request.headers.get('x-forwarded-for') || request.headers.get('client-ip')}`);
      // Return a fake success response to fool the bot
      return NextResponse.json(
        {
          success: true,
          message: 'Ďakujeme! Vaša správa bola úspešne odoslaná.',
        },
        { status: 200, headers }
      );
    }

    // Verify Turnstile Token
    if (!turnstileToken) {
      return NextResponse.json(
        {
          error: 'Chýba CAPTCHA overenie.',
          details: 'Turnstile token is missing.',
        },
        { status: 400, headers }
      );
    }

    const ip = request.headers.get('x-forwarded-for') || request.headers.get('client-ip') || undefined;
    const isHuman = await verifyTurnstile(turnstileToken, ip);
    if (!isHuman) {
      return NextResponse.json(
        {
          error: 'Neúspešné CAPTCHA overenie. Skúste to prosím znova.',
          details: 'Turnstile verification failed.',
        },
        { status: 400, headers }
      );
    }

    // Extract phone, budget, and subject from message if included
    let cleanMessage = message;
    let phone = '';
    let budget = '';
    let subject = '';

    const phoneMatch = message.match(/Telefón:\s*(.+)/);
    const budgetMatch = message.match(/Rozpočet:\s*(.+)/);
    const subjectMatch = message.match(/Typ projektu:\s*(.+)/);

    if (phoneMatch) phone = phoneMatch[1].trim();
    if (budgetMatch) budget = budgetMatch[1].trim();
    if (subjectMatch) subject = subjectMatch[1].trim();

    cleanMessage = message
      .replace(/\n\nTelefón:.*/, '')
      .replace(/Telefón:.*\n?/, '')
      .replace(/Rozpočet:.*\n?/, '')
      .replace(/Typ projektu:.*\n?/, '')
      .trim();

    // Validate required fields
    if (!name || !email || !cleanMessage) {
      return NextResponse.json(
        {
          error: 'Všetky polia sú povinné.',
          details: 'Name, email, and message are required fields.',
        },
        { status: 400, headers }
      );
    }

    // Email validation
    const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
    if (!emailRegex.test(email)) {
      return NextResponse.json(
        {
          error: 'Neplatná emailová adresa.',
          details: 'Invalid email format.',
        },
        { status: 400, headers }
      );
    }

    const SMTP2GO_API_KEY = process.env.SMTP2GO_API_KEY;
    const SMTP2GO_FROM_EMAIL = process.env.SMTP2GO_FROM_EMAIL;
    const BUSINESS_EMAIL = process.env.BUSINESS_EMAIL || process.env.SMTP2GO_FROM_EMAIL;

    if (!SMTP2GO_API_KEY || !SMTP2GO_FROM_EMAIL || !BUSINESS_EMAIL) {
      console.error('SMTP2GO environment variables not configured.');
      return NextResponse.json(
        {
          error: 'Konfiguračná chyba servera. Prosím, kontaktujte administrátora.',
          details: 'Email service environment variables are not set.',
        },
        { status: 500, headers }
      );
    }

    const emailPayload = {
      api_key: SMTP2GO_API_KEY,
      to: [BUSINESS_EMAIL],
      sender: SMTP2GO_FROM_EMAIL,
      subject: `Nová správa z kontaktného formulára - ${name}`,
      text_body: `
Nová správa z kontaktného formulára AEB Digital

Od: ${name}
Email: ${email}

Správa:
${message}

---
Odoslané z: ${request.headers.get('referer') || 'Unknown'}
IP adresa: ${request.headers.get('x-forwarded-for') || request.headers.get('client-ip') || 'Unknown'}
Čas: ${new Date().toLocaleString('sk-SK', { timeZone: 'Europe/Bratislava' })}
      `,
      html_body: `
<!DOCTYPE html>
<html>
<head>
  <meta charset="UTF-8">
  <style>
    body {
      font-family: 'Montserrat', -apple-system, BlinkMacSystemFont, 'Segoe UI', Arial, sans-serif;
      line-height: 1.6;
      color: #ffffff;
      background-color: #16171A;
      margin: 0;
      padding: 40px 20px;
    }
    .container {
      max-width: 600px;
      margin: 0 auto;
      background-color: #16171A;
      padding: 40px 20px;
    }
    .heading {
      color: #00997d;
      font-size: 28px;
      font-weight: 700;
      margin-bottom: 30px;
      text-align: center;
      font-family: 'Montserrat', -apple-system, BlinkMacSystemFont, 'Segoe UI', Arial, sans-serif;
    }
    .form-container {
      background-color: #212327;
      padding: 40px;
      border-radius: 20px;
      border: 0.5px solid #555555;
    }
    .form-group {
      margin-bottom: 20px;
    }
    .label {
      display: block;
      font-weight: 600;
      color: #ffffff;
      margin-bottom: 8px;
      font-size: 12px;
      font-family: 'Montserrat', -apple-system, BlinkMacSystemFont, 'Segoe UI', Arial, sans-serif;
    }
    .value {
      background-color: #383a3c;
      padding: 12px 16px;
      border-radius: 10px;
      color: #ffffff;
      border: 0.5px solid #555555;
      font-size: 14px;
      font-family: 'Montserrat', -apple-system, BlinkMacSystemFont, 'Segoe UI', Arial, sans-serif;
    }
    .value a {
      color: #00997d;
      text-decoration: none;
    }
    .message-box {
      background-color: #383a3c;
      padding: 15px;
      border-radius: 10px;
      color: #ffffff;
      border: 0.5px solid #555555;
      min-height: 100px;
      white-space: pre-wrap;
      font-size: 14px;
      font-family: 'Montserrat', -apple-system, BlinkMacSystemFont, 'Segoe UI', Arial, sans-serif;
    }
    .footer {
      margin-top: 30px;
      padding-top: 20px;
      border-top: 1px solid #383a3c;
      font-size: 12px;
      color: #888888;
      text-align: center;
      font-family: 'Montserrat', -apple-system, BlinkMacSystemFont, 'Segoe UI', Arial, sans-serif;
    }
  </style>
</head>
<body>
  <div class="container">
    <h2 class="heading">Nová správa z kontaktného formulára</h2>
    <div class="form-container">
      <div class="form-group">
        <div class="label">Meno *</div>
        <div class="value">${name}</div>
      </div>
      <div class="form-group">
        <div class="label">Email *</div>
        <div class="value"><a href="mailto:${email}">${email}</a></div>
      </div>
      ${phone ? `<div class="form-group">
        <div class="label">Telefón</div>
        <div class="value">${phone}</div>
      </div>` : ''}
      ${subject ? `<div class="form-group">
        <div class="label">Typ projektu *</div>
        <div class="value">${subject}</div>
      </div>` : ''}
      ${budget ? `<div class="form-group">
        <div class="label">Rozpočet</div>
        <div class="value">${budget}</div>
      </div>` : ''}
      <div class="form-group">
        <div class="label">Správa *</div>
        <div class="message-box">${cleanMessage.replace(/\n/g, '<br>')}</div>
      </div>
      <div class="footer">
        <p>Odoslané z: ${request.headers.get('referer') || 'Unknown'}<br>
        IP: ${request.headers.get('x-forwarded-for') || request.headers.get('client-ip') || 'Unknown'}<br>
        ${new Date().toLocaleString('sk-SK', { timeZone: 'Europe/Bratislava' })}</p>
      </div>
    </div>
  </div>
</body>
</html>
      `
    };

    const smtp2goResponse = await fetch('https://api.smtp2go.com/v3/email/send', {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        'X-Smtp2go-Api-Key': SMTP2GO_API_KEY,
      },
      body: JSON.stringify(emailPayload),
    });

    const result = await smtp2goResponse.json();

    if (smtp2goResponse.ok && result.data && result.data.succeeded > 0) {
      console.log('Email sent successfully:', result);
      return NextResponse.json(
        {
          success: true,
          message: 'Ďakujeme! Vaša správa bola úspešne odoslaná.',
          details: 'Message sent successfully via SMTP2GO.',
        },
        { status: 200, headers }
      );
    } else {
      console.error('SMTP2GO API error:', result);
      return NextResponse.json(
        {
          error: 'Chyba pri odosielaní emailu.',
          details: result.data?.error || 'Failed to send email via SMTP2GO.',
        },
        { status: 500, headers }
      );
    }
  } catch (error: any) {
    console.error('Error processing contact form:', error);
    return NextResponse.json(
      {
        error: 'Nastala chyba pri spracovaní vašej správy.',
        details: error.message || 'Internal server error.',
      },
      { status: 500, headers }
    );
  }
}

// Handle OPTIONS requests for CORS preflight
export async function OPTIONS() {
  const headers = {
    'Access-Control-Allow-Origin': '*', // Adjust as needed for your deployment
    'Access-Control-Allow-Methods': 'POST, OPTIONS',
    'Access-Control-Allow-Headers': 'Content-Type',
  };
  return new NextResponse(null, { status: 204, headers });
}
